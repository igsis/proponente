						<p align="left">O objetivo é credenciar os artistas locais, para que eles possam trabalhar em eventos realizados ou apoiados pela Secretaria de Cultura, além de promover uma aproximação entre o poder público e os agentes culturais. Outra meta, que valoriza e fomenta a produção cultural local, é mapear os artistas e instituições artísticas presentes em Mogi das Cruzes.</p>

