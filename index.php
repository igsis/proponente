						<p align="left">Podem se cadastrar artistas ou grupos artísticos, como pessoa física ou jurídica, diretamente ou então por intermédio do empresário ou produtor. Empresários e produtores, aliás, também podem se cadastrar, bem como qualquer pessoa física ou jurídica que tenha experiência profissional em áreas relacionadas à cultura, tais como oficineiros, produtores de eventos, técnicos de gravação, técnicos de som, iluminotécnicos, monitores, curadores de exposição, jurados, etc.</p>

						<p align="left">Uma vez no cadastro, todos os inscritos passarão por uma avaliação e terão uma posição específica dentro da listagem. Esta avaliação vai levar em conta critérios como originalidade, experiência comprovada, clareza, relevância, localidade e material enviado. O chamamento posterior desses artistas vai obedecer à classificação apontada pelo cadastro.</p>
						
